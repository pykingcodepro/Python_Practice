# Operators
## Arithmetic Operators
'''
    + -> to add 
    - -> to subtract 
    * -> to multiply 
    / -> to divide 
    % -> to take remainder
    // -> for float division
    ** -> for exponention
'''

print(10 + 20) # 10+20 = 30
print(20 - 10) # 20-10 = 10
print(20 * 10) # 20*10 = 200
print(20 / 10) # 20/10 = 2.0
print(20 // 10) # 20//10 = 2
print(2 ** 5) # 2**5 = 32
